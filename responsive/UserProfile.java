package com.example.responsive;

public class UserProfile {
    public String Name;
    public String Age;
    public String Email;
    public String Password;
    public String Gender;

    public UserProfile(String name, String age, String email, String password, String gender) {
        Name = name;
        Age = age;
        Email = email;
        Password = password;
        Gender = gender;
    }
}
